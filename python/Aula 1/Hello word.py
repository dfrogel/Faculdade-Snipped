nome = input("Digite um nome: ")  # Solicita ao usuário que digite um nome e armazena o valor na variável "nome"
print("Olá %s, seja bem-vindo(a) à disciplina de Python. Parabéns pelo seu primeiro hello world!" % nome)
# Imprime uma mensagem de boas-vindas junto com o nome fornecido pelo usuário utilizando formatação de string

# Neste código em Python, a função input() é utilizada para receber a entrada do usuário, 
# que é armazenada na variável nome. Em seguida, a função print() é usada para 
# exibir uma mensagem de boas-vindas, utilizando a formatação de string % para inserir 
# o valor da variável nome na mensagem.
