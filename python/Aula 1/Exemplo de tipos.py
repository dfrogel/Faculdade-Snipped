print("olá")
# string
print(10)
# inteiro
print(3.141592)
# float


# EXPLICAÇÃO DO CÓDIGO 

# Na primeira linha, a função print() exibe o texto "olá". 
# O texto é colocado entre aspas duplas para indicar que é uma string.

# Na segunda linha, a função print() exibe o número 10. 
# Como não está entre aspas, é considerado um valor numérico inteiro.

# Na terceira linha, a função print() exibe o número 3.141592. 
# Esse valor é uma representação de ponto flutuante, também conhecido como float.
