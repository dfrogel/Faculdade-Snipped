# Realizando operações matemáticas

# Operação 1: multiplicação antes da adição
operacao_1 = 2 + 3 * 5  # Resultado esperado: 17

# Operação 2: uso de parênteses para alterar a ordem de avaliação
operacao_2 = (2 + 3) * 5  # Resultado esperado: 25

# Operação 3: divisão e multiplicação em sequência
operacao_3 = 4 / 2 * 2  # Resultado esperado: 4

# Operação 4: uso do operador de resto da divisão
operacao_4 = 13 % 3 + 4  # Resultado esperado: 5

# Exibindo os resultados das operações
print("Resultado da operação_1 =", operacao_1)
print("Resultado da operação_2 =", operacao_2)
print("Resultado da operação_3 =", operacao_3)
print("Resultado da operação_4 =", operacao_4)
