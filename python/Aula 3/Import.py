import math

# Importando todas as funcionalidades da biblioteca 'math'

# Calculando a raiz quadrada de 25 usando a função sqrt() da biblioteca math
raiz_quadrada = math.sqrt(25)
print("Raiz quadrada de 25:", raiz_quadrada)

# Calculando o logaritmo na base 2 de 1024 usando a função log2() da biblioteca math
logaritmo_base2 = math.log2(1024)
print("Logaritmo na base 2 de 1024:", logaritmo_base2)

# Calculando o cosseno de 45 graus usando a função cos() da biblioteca math
cosseno = math.cos(math.radians(45))  # É necessário converter graus para radianos usando radians()
print("Cosseno de 45 graus:", cosseno)
