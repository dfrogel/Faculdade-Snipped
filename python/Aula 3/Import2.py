import math as m

# Importando todas as funcionalidades da biblioteca 'math' com alias 'm'

raiz_quadrada = m.sqrt(25)
print("Raiz quadrada de 25:", raiz_quadrada)

logaritmo_base2 = m.log2(1024)
print("Logaritmo na base 2 de 1024:", logaritmo_base2)

cosseno = m.cos(m.radians(45))
print("Cosseno de 45 graus:", cosseno)
